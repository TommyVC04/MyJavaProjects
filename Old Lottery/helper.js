class Student {

    constructor() {
        this.id = 0;
        this.period = 0;
    }

    constructor(id, name, period) {
        this.id = id;
        this.name = name;
        this.period = period
    }

    getID() {
        return this.id;
    }

    setID(id) {
        this.id = id;
    }

    getPeriod() {
        return this.period;
    }

    setPeriod(period) {
        this.period = period;
    }

    getName() {
        return this.name;
    }

    setName(name) {
        this.name = name;
    }

    getGrpNum() {
        return this.grpNum;
    }

    setGrpNum(num) {
        this.grpNum = num;
    }

}
